import {render} from "testUtils";
import {Avatar} from "../Avatar";

describe("Avatar", () => {
  it("properties will be generated by the seed", () => {
    const {container} = render(<Avatar seed="2" />);
    expect(container).toMatchSnapshot();
  });

  it("seed will generate facial hair", () => {
    const {container} = render(<Avatar seed="1" />);
    expect(container.querySelector("[id^=Facial]")).toBeInTheDocument();
  });

  it("seed will leave out facial hair", () => {
    const {container} = render(<Avatar seed="0" />);
    expect(container.querySelector("[id^=Facial]")).not.toBeInTheDocument();
  });

  it("seed will generate accessories", () => {
    const {container} = render(<Avatar seed="2" />);
    expect(container.querySelector("[id^='Top/_Resources']")).toBeInTheDocument();
  });

  it("seed will leave out accessories", () => {
    const {container} = render(<Avatar seed="0" />);
    expect(container.querySelector("[id^='Top/_Resources']")).not.toBeInTheDocument();
  });

  it("seed will generate facial hair and accessories", () => {
    const {container} = render(<Avatar seed="2" />);
    expect(container.querySelector("[id^=Facial]")).toBeInTheDocument();
    expect(container.querySelector("[id^='Top/_Resources']")).toBeInTheDocument();
  });

  it("seed will generate facial hair but leave out accessories", () => {
    const {container} = render(<Avatar seed="1" />);
    expect(container.querySelector("[id^=Facial]")).toBeInTheDocument();
    expect(container.querySelector("[id^='Top/_Resources']")).not.toBeInTheDocument();
  });

  it("seed will leave out facial hair but generate accessories", () => {
    const {container} = render(<Avatar seed="3" />);
    expect(container.querySelector("[id^=Facial]")).not.toBeInTheDocument();
    expect(container.querySelector("[id^='Top/_Resources']")).toBeInTheDocument();
  });

  it("seed will leave out facial hair and accessories", () => {
    const {container} = render(<Avatar seed="0" />);
    expect(container.querySelector("[id^=Facial]")).not.toBeInTheDocument();
    expect(container.querySelector("[id^='Top/_Resources']")).not.toBeInTheDocument();
  });

  it("seed will define an accent color class", () => {
    const {container} = render(<Avatar seed="2" />);
    expect(container.firstChild).toHaveClass("accent-color__planning-pink");
  });
});
